package com.example.sns_project;

public class BottomDTO {
    private String name;
    private int temp_high;
    private int temp_low;
    private int spring;
    private int summer;
    private int fall;
    private int winter;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getTemp_high() {
        return temp_high;
    }

    public void setTemp_high(int temp_high) {
        this.temp_high = temp_high;
    }

    public int getTemp_low() {
        return temp_low;
    }

    public void setTemp_low(int temp_low) {
        this.temp_low = temp_low;
    }

    public int getSpring() {
        return spring;
    }

    public void setSpring(int spring) {
        this.spring = spring;
    }

    public int getSummer() {
        return summer;
    }

    public void setSummer(int summer) {
        this.summer = summer;
    }

    public int getFall() {
        return fall;
    }

    public void setFall(int fall) {
        this.fall = fall;
    }

    public int getWinter() {
        return winter;
    }

    public void setWinter(int winter) {
        this.winter = winter;
    }

}
